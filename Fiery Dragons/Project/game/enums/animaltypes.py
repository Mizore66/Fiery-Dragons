from enum import Enum


class AnimalType(Enum):
    """
    Author: Yash Mahmud (32945795)
    
    An enumeration class for the different types of animals.

    Attributes
    ----------
    BAT : int
        Represents the bat animal type.
    EGG : int
        Represents the egg animal type.
    SALAMANDER : int
        Represents the salamander animal type.
    SPIDER : int
        Represents the spider animal type.
    PIRATE : int
        Represents the pirate animal type.
    KNIGHT : int
        Represents the knight animal type.
    """
    BAT = 0
    EGG = 1
    SALAMANDER = 2
    SPIDER = 3
    PIRATE = 4
    KNIGHT = 5
